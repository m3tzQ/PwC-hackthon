export function getPlayerById(playerId: number): Player | undefined {
  return players.find((player) => player.id === playerId)
}

export function getTopOptionsByPosition(position: PositionKey, limit = 3): Player[] {
  const preferredIds = positionPriorityMap[position] ?? []
  const rankedPlayers = preferredIds
    .map((playerId) => getPlayerById(playerId))
    .filter((player): player is Player => Boolean(player))
    .sort((a, b) => b.recommendationScore - a.recommendationScore)

  return rankedPlayers.slice(0, limit)
}

export function getTopReplacementCandidates(limit = 10): Player[] {
  return players
    .filter((player) => player.availability !== 'Out')
    .sort((a, b) => b.recommendationScore - a.recommendationScore)
    .slice(0, limit)
}

export function getHistoryByPlayer(playerId: number): MatchStatLine[] {
  return playerHistory.filter((entry) => entry.playerId === playerId)
}
